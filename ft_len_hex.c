#include "ft_printf.h"

int		ft_len_hex(unsigned long nb)
{
	int i;

	i = 0;
	if (nb == 0)
	{
		i = 1;
		return (i);
	}
	while (nb != 0)
	{
		i++;
		nb /= 16;
	}
	return (i);
}
